
module Instruction_memory_2X32(
    input  [31:0] read_address,
    output [31:0] instruction_out
);
    reg [7:0] memory [0:4095];

    integer i;
    initial begin
        // Initialize ALL to NOP (0)
        for (i = 0; i < 4096; i = i + 1)
            memory[i] = 8'h00;

        // ============================================================
        // Instruction 0 : lw $r8, 1200($r9)
        //   op=100011, rs=$r9=01001, rt=$r8=01000, imm=0x04B0
        //   1000_1101  0010_1000  0000_0100  1011_0000
        // ============================================================
        memory[0]  = 8'b10001101;
        memory[1]  = 8'b00101000;
        memory[2]  = 8'b00000100;
        memory[3]  = 8'b10110000;

        // ============================================================
        // Instruction 1 : lw $r9, 1200($r9)
        //   op=100011, rs=$r9=01001, rt=$r9=01001, imm=0x04B0
        //   1000_1101  0010_1001  0000_0100  1011_0000
        // ============================================================
        memory[4]  = 8'b10001101;
        memory[5]  = 8'b00101001;
        memory[6]  = 8'b00000100;
        memory[7]  = 8'b10110000;

        // ============================================================
        // Instruction 2 : add $r10, $r8, $r9
        //   op=000000, rs=$r8=01000, rt=$r9=01001, rd=$r10=01010, shamt=00000, func=100000
        //   0000_0001  0000_1001  0101_0000  0010_0000
        // ============================================================
        memory[8]  = 8'b00000001;
        memory[9]  = 8'b00001001;
        memory[10] = 8'b01010000;
        memory[11] = 8'b00100000;

        // ============================================================
        // Instruction 3 : sw $r10, 20($r9)
        //   op=101011, rs=$r9=01001, rt=$r10=01010, imm=20=0x0014
        //   1010_1101  0010_1010  0000_0000  0001_0100
        // ============================================================
        memory[12] = 8'b10101101;
        memory[13] = 8'b00101010;
        memory[14] = 8'b00000000;
        memory[15] = 8'b00010100;

        // ============================================================
        // Instruction 4 : sub $r11, $r8, $r9
        //   op=000000, rs=$r8=01000, rt=$r9=01001, rd=$r11=01011, shamt=00000, func=100010
        //   0000_0001  0000_1001  0101_1000  0010_0010
        // ============================================================
        memory[16] = 8'b00000001;
        memory[17] = 8'b00001001;
        memory[18] = 8'b01011000;
        memory[19] = 8'b00100010;

        // ============================================================
        // Instruction 5 : and $r12, $r8, $r9
        //   op=000000, rs=$r8=01000, rt=$r9=01001, rd=$r12=01100, shamt=00000, func=100100
        //   0000_0001  0000_1001  0110_0000  0010_0100
        // ============================================================
        memory[20] = 8'b00000001;
        memory[21] = 8'b00001001;
        memory[22] = 8'b01100000;
        memory[23] = 8'b00100100;

        // ============================================================
        // Instruction 6 : or $r13, $r8, $r9
        //   op=000000, rs=$r8=01000, rt=$r9=01001, rd=$r13=01101, shamt=00000, func=100101
        //   0000_0001  0000_1001  0110_1000  0010_0101
        // ============================================================
        memory[24] = 8'b00000001;
        memory[25] = 8'b00001001;
        memory[26] = 8'b01101000;
        memory[27] = 8'b00100101;

        // ============================================================
        // Instruction 7 : beq $r8, $r9, +2  (branch forward 2 instructions if $r8==$r9)
        //   op=000100, rs=$r8=01000, rt=$r9=01001, offset=0x0002
        //   Byte[31:24] = 000100_01 = 0001_0001
        //   Byte[23:16] = 000_01001 = 0000_1001
        //   Byte[15:8]  = 00000000
        //   Byte[7:0]   = 00000010
        // ============================================================
        memory[28] = 8'b00010001;
        memory[29] = 8'b00001001;
        memory[30] = 8'b00000000;
        memory[31] = 8'b00000010;

        // ============================================================
        // Instruction 8 : bne $r8, $r9, +2  (branch forward 2 instructions if $r8!=$r9)
        //   op=000101, rs=$r8=01000, rt=$r9=01001, offset=0x0002
        //   Byte[31:24] = 000101_01 = 0001_0101
        //   Byte[23:16] = 000_01001 = 0000_1001
        //   Byte[15:8]  = 00000000
        //   Byte[7:0]   = 00000010
        // ============================================================
        memory[32] = 8'b00010101;
        memory[33] = 8'b00001001;
        memory[34] = 8'b00000000;
        memory[35] = 8'b00000010;

        // ============================================================
        // Instruction 9 : j 0x00000010  (jump to instruction at byte address 0x40 = instr 16)
        //   op=000010, target[25:0] = 26-bit word address = 0x10 >> 2 = 0x04 = 0b00_0000_0000_0000_0000_0000_0100
        //   Byte[31:24] = 000010_00 = 0000_1000
        //   Byte[23:16] = 00000000
        //   Byte[15:8]  = 00000000
        //   Byte[7:0]   = 00000100
        // ============================================================
        memory[36] = 8'b00001000;
        memory[37] = 8'b00000000;
        memory[38] = 8'b00000000;
        memory[39] = 8'b00000100;

    end

    // Word-aligned big-endian read with bounds check
    wire [31:0] addr;
    assign addr = read_address;

    assign instruction_out = (addr <= 32'd4092) ?
        { memory[addr], memory[addr+1], memory[addr+2], memory[addr+3] }
        : 32'b0;

endmodule
